package cn.tedu.comic.entity;

import java.io.Serializable;
import java.util.Date;

public class UserBook implements Serializable{

	/**
	 * 
	 */
	private static final long serialVersionUID = -8815368773315569350L;
	private Integer id;
	private Integer uid;
	private String bookname;
	private Date createTime;
	private Integer pass;//后台管理员是否通过
	private String url;//图书路径
	private String image1url;
	private String image2url;
	private String image3url;
	public Integer getId() {
		return id;
	}
	public void setId(Integer id) {
		this.id = id;
	}
	public Integer getUid() {
		return uid;
	}
	public void setUid(Integer uid) {
		this.uid = uid;
	}
	public String getBookname() {
		return bookname;
	}
	public void setBookname(String bookname) {
		this.bookname = bookname;
	}
	public Date getCreateTime() {
		return createTime;
	}
	public void setCreateTime(Date createTime) {
		this.createTime = createTime;
	}
	public Integer getPass() {
		return pass;
	}
	public void setPass(Integer pass) {
		this.pass = pass;
	}
	public String getUrl() {
		return url;
	}
	public void setUrl(String url) {
		this.url = url;
	}
	public String getImage1url() {
		return image1url;
	}
	public void setImage1url(String image1url) {
		this.image1url = image1url;
	}
	public String getImage2url() {
		return image2url;
	}
	public void setImage2url(String image2url) {
		this.image2url = image2url;
	}
	public String getImage3url() {
		return image3url;
	}
	public void setImage3url(String image3url) {
		this.image3url = image3url;
	}
	@Override
	public String toString() {
		return "UserBook [id=" + id + ", uid=" + uid + ", bookname=" + bookname + ", createTime=" + createTime
				+ ", pass=" + pass + ", url=" + url + ", image1url=" + image1url + ", image2url=" + image2url
				+ ", image3url=" + image3url + "]";
	} 
	
}
