package cn.tedu.comic.entity;

import java.io.Serializable;

/**
 * 该类的每一个实例代表一条收藏记录
 * 
 * @author Administrator
 *
 */
public class collect implements Serializable {

	private static final long serialVersionUID = -7131434740570656078L;

	private Integer id; // 该条记录的id
	private Integer uid; // 用户的id
	private String bookname; // 书的名字
	private String cover; // 书的图片
	private String url; // 书的路径

	public collect() {
		super();
	}

	public collect(Integer id, Integer uid, String bookname, String cover, String url) {
		super();
		this.id = id;
		this.uid = uid;
		this.bookname = bookname;
		this.cover = cover;
		this.url = url;
	}

	public Integer getId() {
		return id;
	}

	public void setId(Integer id) {
		this.id = id;
	}

	public Integer getUid() {
		return uid;
	}

	public void setUid(Integer uid) {
		this.uid = uid;
	}

	public String getBookname() {
		return bookname;
	}

	public void setBookname(String bookname) {
		this.bookname = bookname;
	}

	public String getCover() {
		return cover;
	}

	public void setCover(String cover) {
		this.cover = cover;
	}

	public String getUrl() {
		return url;
	}

	public void setUrl(String url) {
		this.url = url;
	}

	@Override
	public String toString() {
		return "collect [id=" + id + ", uid=" + uid + ", bookname=" + bookname + ", cover=" + cover + ", url=" + url
				+ "]";
	}

}
