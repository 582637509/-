package cn.tedu.comic.service.ex;

public class InsertDateException extends ServiceException{

	/**
	 * 增删改未知异常
	 */
	private static final long serialVersionUID = -2835333888140753895L;

	public InsertDateException() {
		super();
	}

	public InsertDateException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

	public InsertDateException(String message, Throwable cause) {
		super(message, cause);
	}

	public InsertDateException(String message) {
		super(message);
	}

	public InsertDateException(Throwable cause) {
		super(cause);
	}
	
}
